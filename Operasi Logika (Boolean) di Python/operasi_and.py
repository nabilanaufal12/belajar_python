# Operasi AND (Konjungsi)
print("Operasi AND:")
a = True
b = True
hasil = a and b
print(a, " AND ", b, " =", hasil)

a = True
b = False
hasil = a and b
print(a, " AND ", b, " =", hasil)

a = False
b = True
hasil = a and b
print(a, " AND ", b, " =", hasil)

a = False
b = False
hasil = a and b
print(a, " AND ", b, " =", hasil)