#Cara Penggunaan Tipe Data Number Python
foo = 100   #integer
bar = 30.23 #float
baz = 4j    #complex number

print(foo)
print(bar)
print(baz)

#Untuk melihat tipe data
print(type(foo))
print(type(bar))
print(type(baz))

#Nilai maksimum untuk tipe data number
#Integer sama seperti long
#Float sama seperti double

#Penulisan notasi ilmiah (Scientific Notation)
foo = 3e2
bar = 3e-2
baz = 1.34e5

print(foo)
print(bar)
print(baz)
