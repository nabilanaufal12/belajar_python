# Operasi Aritmatika dalam f-string pada Python

# Penjumlahan
a = 10
b = 5
print(f"Hasil penjumlahan {a} + {b} = {a + b}")

# Pengurangan
print(f"Hasil pengurangan {a} - {b} = {a - b}")

# Perkalian
print(f"Hasil perkalian {a} * {b} = {a * b}")

# Pembagian
print(f"Hasil pembagian {a} / {b} = {a / b}")

# Modulus (Sisa Pembagian)
print(f"Sisa pembagian {a} % {b} = {a % b}")

# Pangkat (Eksponen)
print(f"{a} pangkat 2 = {a ** 2}")