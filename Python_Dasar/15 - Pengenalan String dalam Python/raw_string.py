# Raw String (Literal String)

# Raw string tidak menginterpretasikan karakter escape
raw_string = r"Halo, \n Dunia!"
print(raw_string)  # Output: Halo, \n Dunia!

# Contoh penggunaan raw string dalam path file
path_file = r"C:\Users\Name\Documents\file.txt"
print(path_file)  # Output: C:\Users\Name\Documents\file.txt