"""
    Cara Membuat String dalam Python
    1. Menggunakan Tanda Kutip Tunggal ('...')
    2. Menggunakan Tanda Kutip Ganda ("...")
    3. Menggabungkan Tanda Kutip
"""

# 1. Menggunakan Tanda Kutip Tunggal
string_tunggal = 'Halo, Dunia!'
print(string_tunggal)  # Output: Halo, Dunia!

# 2. Menggunakan Tanda Kutip Ganda
string_ganda = "Halo, Dunia!"
print(string_ganda)  # Output: Halo, Dunia!

# 3. Menggabungkan Tanda Kutip
string_gabungan = 'Halo, "Dunia"!'
print(string_gabungan)  # Output: Halo, "Dunia"!
string_gabungan2 = "Halo, 'Dunia'!"
print(string_gabungan2)  # Output: Halo, 'Dunia'!