# Operasi OR (Disjungsi)
print("Operasi OR:")
a = True
b = True
hasil = a or b
print(a, " OR ", b, " =", hasil)

a = True
b = False
hasil = a or b
print(a, " OR ", b, " =", hasil)

a = False
b = True
hasil = a or b
print(a, " OR ", b, " =", hasil)

a = False
b = False
hasil = a or b
print(a, " OR ", b, " =", hasil)