# Operasi XOR (Eksklusif Disjungsi)
print("Operasi XOR:")
a = True
b = True
hasil = a ^ b
print(a, " XOR ", b, " =", hasil)

a = True
b = False
hasil = a ^ b
print(a, " XOR ", b, " =", hasil)

a = False
b = True
hasil = a ^ b
print(a, " XOR ", b, " =", hasil)

a = False
b = False
hasil = a ^ b
print(a, " XOR ", b, " =", hasil)