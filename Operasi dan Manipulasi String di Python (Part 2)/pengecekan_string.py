# Pengecekan String (isX Methods)

data = "Halo Dunia"
print("Data asli:", data)
print("Apakah data adalah huruf besar?", data.isupper())
print("Apakah data adalah huruf kecil?", data.islower())
print("Apakah data adalah huruf?", data.isalpha())
print("Apakah data adalah angka?", data.isdigit())
print("Apakah data adalah huruf atau angka?", data.isalnum())
print("Apakah data adalah spasi?", data.isspace())
print("Apakah data dalam format title?", data.istitle())
print("Apakah semua karakter adalah printable?", data.isprintable())
print("Apakah semua karakter adalah decimal?", data.isdecimal())