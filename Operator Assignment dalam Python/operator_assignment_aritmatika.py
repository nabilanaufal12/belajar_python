a = 5
print("Nilai a:", a)

# Penjumlahan dengan operator assignment
print("\nPenjumlahan (operator +=)")
a += 3  # Sama dengan a = a + 3
print("Setelah a += 3, nilai a:", a)

# Pengurangan dengan operator assignment
print("\nPengurangan (operator -=)")
a = 5
a -= 2  # Sama dengan a = a - 2
print("Setelah a -= 2, nilai a:", a)

# Perkalian dengan operator assignment
print("\nPerkalian (operator *=)")
a = 5
a *= 4  # Sama dengan a = a * 4
print("Setelah a *= 4, nilai a:", a)

# Pembagian dengan operator assignment
print("\nPembagian (operator /=)")
a = 5
a /= 2  # Sama dengan a = a / 2
print("Setelah a /= 2, nilai a:", a)

# Modulus dengan operator assignment
print("\nModulus (operator %=)")
a = 5
a %= 2  # Sama dengan a = a % 2
print("Setelah a %= 2, nilai a:", a)

# Floor Division dengan operator assignment
print("\nFloor Division (operator //=)")
a = 5
a //= 2  # Sama dengan a = a // 2
print("Setelah a //= 2, nilai a:", a)

# Eksponen dengan operator assignment
print("\nEksponen (operator **=)")
a = 5
a **= 2  # Sama dengan a = a ** 2
print("Setelah a **= 2, nilai a:", a)