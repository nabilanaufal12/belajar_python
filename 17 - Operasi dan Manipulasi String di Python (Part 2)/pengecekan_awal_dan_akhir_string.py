# Pengecekan Awal dan Akhir String di Python

data = "Halo Dunia"
print("Data asli:", data)
print("Apakah data dimulai dengan 'Halo'?", data.startswith("Halo"))
print("Apakah data diakhiri dengan 'Dunia'?", data.endswith("Dunia"))
print("Apakah data dimulai dengan 'Dunia'?", data.startswith("Dunia"))
print("Apakah data diakhiri dengan 'Halo'?", data.endswith("Halo"))