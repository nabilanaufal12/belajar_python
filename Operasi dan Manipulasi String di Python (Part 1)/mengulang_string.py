# Mengulang String (*) di Python

nama_belakang = "Santoso"
nama_tengah = "D"
nama_depan = "Bell"

print("Nama belakang :", nama_belakang)
print("Nama tengah  :", nama_tengah)
print("Nama depan   :", nama_depan)

# Menyambung string menggunakan operator +
nama_lengkap = nama_belakang + " " + nama_tengah + "'" + nama_depan
print("Nama lengkap menggunakan + :", nama_lengkap)

# Mengulang string menggunakan operator *
print("Mengulang nama depan 3 kali :", nama_depan * 3)
print("Mengulang nama tengah 5 kali :", nama_tengah * 5)
print("Mengulang nama belakang 2 kali :", nama_belakang * 2)