#Tipe Data String (1)
foo = "Duniailkom"
print(foo)
bar = 'Duniailkom'
print(bar)
print("\n")

#Tipe Data String (2)
foo = "Teks ini akan dipecah\nke dalam 2 baris"
print(foo)
foo = 'Teks ini\nakan dipecah\nke dalam 3 baris'
print(foo)
print("\n")

#Tipe Data String (3)
foo = '''Teks ini
akan dipecah
ke dalam 3 baris'''
print(foo)
print("\n")

#Operasi Penyambung String (string concatenation)
foo = 'Belajar '
bar = "Bahasa Pemrograman Python "
baz = "di Duniailkom"
hasil = foo + bar + baz
print(hasil)
print("\n")

#String Python sebagai Array
foo = 'Duniailkom'
print(foo[0])
print(foo[4])
print(foo[5:10])
