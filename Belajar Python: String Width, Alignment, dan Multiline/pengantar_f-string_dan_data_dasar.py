# Pengantar F-String dan Data Dasar

# Sebagai contoh, kita akan menggunakan data berikut:
data_nama = "Alice"
data_umur = 30
data_tinggi = 165.5
data_nomor_telepon = "081234567890"

# Secara standar, tanpa formatting khusus, output string akan ditampilkan dalam satu baris panjang:
print(5*"=" + " Output Standar " + 5*"=")
print(f"Nama: {data_nama}, Umur: {data_umur}, Tinggi: {data_tinggi} cm, Nomor Telepon: {data_nomor_telepon}")