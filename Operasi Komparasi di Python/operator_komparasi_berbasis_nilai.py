a = 4
b = 3

print("Nilai a:", a)
print("Nilai b:", b)

# Lebih besar dari (>)
print("\nOperasi Komparasi: Lebih besar dari (>)")
hasil = a > b
print("a > b:", hasil)
hasil = b > a
print("b > a:", hasil)

# Lebih kecil dari (<)
print("\nOperasi Komparasi: Lebih kecil dari (<)")
hasil = a < b
print("a < b:", hasil)
hasil = b < a
print("b < a:", hasil)

# Lebih besar dari atau sama dengan (>=)
print("\nOperasi Komparasi: Lebih besar dari atau sama dengan (>=)")
hasil = a >= b
print("a >= b:", hasil)
hasil = b >= a
print("b >= a:", hasil)

# Lebih kecil dari atau sama dengan (<=)
print("\nOperasi Komparasi: Lebih kecil dari atau sama dengan (<=)")
hasil = a <= b
print("a <= b:", hasil)
hasil = b <= a
print("b <= a:", hasil)

# Sama dengan (==)
print("\nOperasi Komparasi: Sama dengan (==)")
hasil = a == b
print("a == b:", hasil)
hasil = b == a
print("b == a:", hasil)

# Tidak sama dengan (!=)
print("\nOperasi Komparasi: Tidak sama dengan (!=)")
hasil = a != b
print("a != b:", hasil)
hasil = b != a
print("b != a:", hasil)