# Format Basis Angka Lain dengan f-string pada Python

# Bilangan Biner
nilai_biner = 10
print(f"Nilai biner dari {nilai_biner} adalah {bin(nilai_biner)}")

# Bilangan Oktal
nilai_oktal = 10
print(f"Nilai oktal dari {nilai_oktal} adalah {oct(nilai_oktal)}")

# Bilangan Heksadesimal
nilai_heksadesimal = 10
print(f"Nilai heksadesimal dari {nilai_heksadesimal} adalah {hex(nilai_heksadesimal)}")