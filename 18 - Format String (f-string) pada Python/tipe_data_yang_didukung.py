# Tipe Data yang Didukung oleh f-string pada Python

# String
nama = "Alice"
print(f"Nama saya adalah {nama}")

# Integer
umur = 30
print(f"Saya berumur {umur} tahun")

# Float
tinggi = 175.5
print(f"Tinggi saya adalah {tinggi} cm")

# Boolean
is_student = True
print(f"Saya adalah seorang mahasiswa: {is_student}")

# List
hobi = ["membaca", "menulis", "bermain game"]
print(f"Hobi saya adalah {hobi}")

# Dictionary
data_diri = {"nama": "Alice", "umur": 30, "kota": "Jakarta"}
print(f"Data diri saya: {data_diri}")